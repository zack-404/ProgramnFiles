package class8;
import java.lang.*;
public class Math {
	public static void main(String[] args) {
		System.out.println(java.lang.Math.round(3.56));//round
		System.out.println(java.lang.Math.round(3.46));//round
		System.out.println(java.lang.Math.floor(3.46));//floor
		System.out.println(java.lang.Math.floor(3.46));//floor
		System.out.println(java.lang.Math.ceil(3.46));//ceil
		System.out.println(java.lang.Math.ceil(3.46));//ceil
		
	}
}

package operator;

public class normalOpertor {
	public static void main(String[] args) {
		System.out.println(2+3);//相加
		System.out.println(2-3);//相減
		System.out.println(2*3);//乘法
		System.out.println(Math.multiplyExact(2,3));//乘法
		System.out.println(3/2);//除法
		System.out.println(Math.multiplyExact(3,1/2));//乘法
		System.out.println((double)(3/2));//轉型
		System.out.println(3%2);//餘數
	}
}
